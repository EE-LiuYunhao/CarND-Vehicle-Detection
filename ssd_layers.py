def normalize_optim(network):
    bias_p, weight_p = [],[]
    for name, p in network.named_parameters():
        if 'bias' in name:
            bias_p += [p]
        else: 
            weight_p += [p]
    return torch.optim.SGD([{'params': weight_p, 'weight_decay':1e-5},
                            {'params': bias_p, 'weight_decay':0}], 
                            lr=LEARNING_RATE, 
                            momentum=MOMENTUM)
